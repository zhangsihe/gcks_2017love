    public function made_png(){
        $im = imagecreatetruecolor(621, 621);
        $red = imagecolorallocate($im, 192, 29, 29);
        $black = imagecolorallocate($im, 255, 255, 255);
        imagefilledrectangle($im, 0, 0, 621, 621, $red);

        $font_file = './font.ttf';

        // 读取预定义配置信息输出文字到图片上
        if($this->sex == 0){
            $sex_text = $this->woman_text;
        }else{
            $sex_text = $this->man_text;
        }
        $rand = array_rand($sex_text,1);
        for($i = 0; $i < sizeof($sex_text[$rand]) ; $i++){
            $text = str_replace("{name}", $this->name, $sex_text[$rand][$i]['text']);
            // 加粗
            if(isset($sex_text[$rand][$i]['bold'])){
                imagefttext($im, $sex_text[$rand][$i]['size'], 0, $sex_text[$rand][$i]['x'], $sex_text[$rand][$i]['y']-1, $black, $font_file, $text);
                imagefttext($im, $sex_text[$rand][$i]['size'], 0, $sex_text[$rand][$i]['x'], $sex_text[$rand][$i]['y']+1, $black, $font_file, $text);
                imagefttext($im, $sex_text[$rand][$i]['size'], 0, $sex_text[$rand][$i]['x']-1, $sex_text[$rand][$i]['y'], $black, $font_file, $text);
                imagefttext($im, $sex_text[$rand][$i]['size'], 0, $sex_text[$rand][$i]['x']+1, $sex_text[$rand][$i]['y'], $black, $font_file, $text);
            }
            imagefttext($im, $sex_text[$rand][$i]['size'], 0, $sex_text[$rand][$i]['x'], $sex_text[$rand][$i]['y'], $black, $font_file, $text);
        }

        // 底部
        imagefttext($im, 16, 0, 153, 565, $black, $font_file, '——2017 '.$this->name.' 的情人节故事');
        imagefttext($im, 16, 0, 200, 595, $black, $font_file, '免费Wi-Fi: '.$this->ssid);
        imagefttext($im, 14, 0, 500, 615, $black, $font_file, '长  按  参  与');

        $code_png_case = imagecreatefromstring(file_get_contents($this->code_png));
        list($src_w, $src_h) = getimagesize($this->code_png);
        imagecopy($im,$code_png_case,500,493,0,0,$src_w,$src_h);

        // 输出确定图片
        $time_seal = date('ymdhis',time()).rand(1,10000).'.png';
        $filename = '../generate/'."$this->d_time/".$time_seal;
        if(!file_exists('../generate/'.$this->d_time)){
            mkdir('../generate/'.$this->d_time);
        }
        ImagePng($im, $filename);
        imagedestroy($im);
        return $time_seal;
        /*header('Content-type: image/png');
        ImagePng($im);
        imagedestroy($im);*/
    }
